# Python Strings

# Strings
print("Hello World" + " Hello World")  # Same Thing

# Quotes insides quotes
print("It's alright")
print("He is called 'Johnny'")
print('He is called "Johnny"')

# Assign String to a Variable
greeting = "Hello"
print(greeting)

# Multiline Strings
lineDoubleQuote = """
Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua.
"""
print(lineDoubleQuote)

lineSingleQuote = '''
Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt
ut labore et dolore magna aliqua.
'''
print(lineSingleQuote)

# Strings are Arrays
a = "Hello World"
print(a[2])

# Looping Through a String
for x in "Leester":
    print(x)
    





